#include <bits/stdc++.h>

using namespace std;

int main (){
    char op;
    cin >> op;
    if(op=='A')
        cout << "1\n";
    else if (op=='B')
        cout << "2\n";
    else if (op=='C')
        cout << "0\n";
    else if (op=='D')
        cout << "3\n";
    else if (op=='E')
        cout << "4\n";
    else if(op=='F')
        cout <<"6\n";
    else
        cout << "9\n";
    return 0;
}
